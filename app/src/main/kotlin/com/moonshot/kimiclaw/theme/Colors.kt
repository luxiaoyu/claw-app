data class Colors(
    val dark: Color,
    val light: Color,
) {
    fun chooseColor(theme: Int): Color {
        return when (theme) {
            DARK -> dark
            LIGHT -> light
            else -> dark
        }
    }

    companion object {
        const val DARK = 0
        const val LIGHT = 1
    }
}
